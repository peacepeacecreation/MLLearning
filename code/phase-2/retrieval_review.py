# Q2 (scaling): який приблизний діапазон дає StandardScaler().fit_transform(x)?
# А який діапазон дав би MinMaxScaler? Спробуй спочатку відповісти, потім
# перевір кодом нижче.
x = np.random.randn(1000) * 50 + 100  # довільні "сирі" дані для перевірки

# твоя відповідь: StandardScaler приблизний діапазон [0, 1], рідко зустрічається [-1, 1].
# MinMaxScaler — тут може бути напевно будь-який діапазон?

# твій код перевірки:

from sklearn.preprocessing import StandardScaler, MinMaxScaler

x_standard = StandardScaler().fit_transform(x.reshape(-1, 1))
x_minmax = MinMaxScaler().fit_transform(x.reshape(-1, 1))

print("StandardScaler:", x_standard.min(), x_standard.max())
print("MinMaxScaler:", x_minmax.min(), x_minmax.max())

# Правильна відповідь: StandardScaler може бути будь який і не рідко вище за [-1, 1] залежить від розподілу даних.
# MinMaxScaler — це завжди [0, 1] незалежно від розподілу даних. Визначається за формулою (x-min)/(max-min)


